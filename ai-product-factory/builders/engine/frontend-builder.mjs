function routeRegistryFile(routePlans, visualDifferentiationMap) {
  const routeRegistry = routePlans.map((plan) => ({
    path: plan.routeId,
    routeId: routeIdFromPath(plan.routeId),
    label: routeLabel(plan.routeId),
    navVisible: !plan.routeId.startsWith('/sign-') && plan.routeId !== '/terms' && plan.routeId !== '/privacy',
    public: !plan.routeId.startsWith('/sign-'),
    heroVariant: visualDifferentiationMap.find((entry) => entry.routeId === plan.routeId)?.heroVariant ?? 'signal-horizon'
  }));

  return [
    `export const routeRegistry = ${json(routeRegistry)} as const;`,
    '',
    'export const primaryNavigation = routeRegistry.filter((route) => route.navVisible);',
    'export const publicRoutes = routeRegistry.filter((route) => route.public);',
    "export const dynamicSurfaceStates = ['live', 'loading', 'error', 'empty', 'offline'] as const;",
    '',
    'export function heroVariantFor(pathname: string) {',
    "  return routeRegistry.find((route) => route.path === pathname)?.heroVariant ?? 'signal-horizon';",
    '}',
    ''
  ].join('\n');
}


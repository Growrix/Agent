import { mkdir, writeFile } from 'node:fs/promises';
import path from 'node:path';

function json(value) {
  return JSON.stringify(value, null, 2);
}

function routeLabel(routePath) {
  const labelMap = {
    '/': 'Home',
    '/product': 'Product',
    '/pricing': 'Pricing',
    '/services': 'Services',
    '/about': 'About',
    '/emergency': 'Emergency',
    '/quote': 'Get Quote',
    '/contact': 'Contact',
    '/terms': 'Terms & Conditions',
    '/privacy': 'Privacy Policy',
    '/sign-in': 'Sign In',
    '/sign-up': 'Sign Up'
  };

  if (labelMap[routePath]) {
    return labelMap[routePath];
  }

  return routePath
    .replace(/^\//, '')
    .split('/')
    .map((part) => part.replace(/-/g, ' '))
    .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
    .join(' ');
}

function routeIdFromPath(routePath) {
  const routeIdMap = {
    '/': 'home',
    '/product': 'product',
    '/pricing': 'pricing',
    '/services': 'services',
    '/about': 'about',
    '/emergency': 'emergency',
    '/quote': 'quote',
    '/contact': 'contact',
    '/terms': 'terms',
    '/privacy': 'privacy',
    '/sign-in': 'sign-in',
    '/sign-up': 'sign-up'
  };

  return routeIdMap[routePath] ?? (routePath.replace(/^\//, '').replace(/\//g, '-') || 'home');
}

async function writeGeneratedFile(appRoot, relativePath, content) {
  const targetPath = path.join(appRoot, relativePath);
  await mkdir(path.dirname(targetPath), { recursive: true });
  await writeFile(targetPath, content, 'utf8');
}


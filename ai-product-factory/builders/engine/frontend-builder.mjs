function themeProviderFile() {
  return [
    "'use client';",
    '',
    "import type { ReactNode } from 'react';",
    "import { createContext, useContext, useEffect, useMemo, useState } from 'react';",
    "import { THEME_STORAGE_KEY, nextTheme, normalizeTheme, type ThemeName } from '../lib/theme';",
    '',
    'type ThemeContextValue = {',
    '  theme: ThemeName;',
    '  mounted: boolean;',
    '  toggleTheme: () => void;',
    '};',
    '',
    'const ThemeContext = createContext<ThemeContextValue | null>(null);',
    '',
    'type ThemeProviderProps = {',
    '  children: ReactNode;',
    '};',
    '',
    'function applyTheme(theme: ThemeName) {',
    "  document.documentElement.setAttribute('data-theme', theme);",
    '}',
    '',
    'function bindMotionPreference() {',
    "  const media = window.matchMedia('(prefers-reduced-motion: reduce)');",
    '  const applyMotion = () => {',
    "    document.documentElement.setAttribute('data-motion', media.matches ? 'reduce' : 'full');",
    '  };',
    '  applyMotion();',
    '  if (typeof media.addEventListener === "function") {',
    "    media.addEventListener('change', applyMotion);",
    "    return () => media.removeEventListener('change', applyMotion);",
    '  }',
    "  media.addListener(applyMotion);",
    '  return () => media.removeListener(applyMotion);',
    '}',
    '',
    'export function ThemeProvider({ children }: ThemeProviderProps) {',
    "  const [theme, setTheme] = useState<ThemeName>('light');",
    '  const [mounted, setMounted] = useState(false);',
    '',
    '  useEffect(() => {',
    '    const storedTheme = normalizeTheme(window.localStorage.getItem(THEME_STORAGE_KEY));',
    '    setTheme(storedTheme);',
    '    applyTheme(storedTheme);',
    '    setMounted(true);',
    '    return bindMotionPreference();',
    '  }, []);',
    '',
    '  useEffect(() => {',
    '    if (!mounted) {',
    '      return;',
    '    }',
    '',
    '    applyTheme(theme);',
    '    window.localStorage.setItem(THEME_STORAGE_KEY, theme);',
    '  }, [mounted, theme]);',
    '',
    '  const value = useMemo(',
    '    () => ({',
    '      theme,',
    '      mounted,',
    '      toggleTheme: () => setTheme((currentTheme) => nextTheme(currentTheme))',
    '    }),',
    '    [mounted, theme]',
    '  );',
    '',
    '  return <ThemeContext.Provider value={value}>{children}</ThemeContext.Provider>;',
    '}',
    '',
    'export function useTheme() {',
    '  const context = useContext(ThemeContext);',
    '  if (!context) {',
    "    throw new Error('useTheme must be used within ThemeProvider.');",
    '  }',
    '  return context;',
    '}',
    ''
  ].join('\n');
}


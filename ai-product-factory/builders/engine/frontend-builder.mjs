function auditScriptFile(routePlans, visualDifferentiationMap, buildPlan) {
  const expectedRoutes = routePlans.map((plan) => plan.routeId);
  const expectedComponents = buildPlan.components;

  return [
    "import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';",
    "import path from 'node:path';",
    "import { fileURLToPath } from 'node:url';",
    '',
    'const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");',
    `const expectedRoutes = ${json(expectedRoutes)};`,
    `const expectedHeroVariants = ${json(visualDifferentiationMap)};`,
    `const expectedComponents = ${json(expectedComponents)};`,
    'const failures = [];',
    'const evidence = [];',
    '',
    'function routePathToFile(routePath) {',
    "  if (routePath === '/') {",
    "    return 'src/app/page.tsx';",
    '  }',
    "  return `src/app${routePath}/page.tsx`;",
    '}',
    '',
    'for (const routePath of expectedRoutes) {',
    '  const relativePath = routePathToFile(routePath);',
    '  const fullPath = path.join(rootDir, relativePath);',
    '  if (!existsSync(fullPath)) {',
    '    failures.push(`Missing route file: ${relativePath}`);',
    '    continue;',
    '  }',
    '  const contents = readFileSync(fullPath, "utf8");',
    '  const routeId = routePath === "/" ? "home" : routePath.replace(/^\\//, "").replace(/\\//g, "-");',
    "  if (!contents.includes(`data-route-id='${routeId}'`) && !contents.includes(`data-route-id=\"${routeId}\"`)) {",
    '    failures.push(`Route marker missing in ${relativePath}`);',
    '  }',
    '  evidence.push(`route:${routePath} -> ${relativePath}`);',
    '}',
    '',
    'const routeRegistryPath = path.join(rootDir, "src/lib/route-registry.ts");',
    'if (!existsSync(routeRegistryPath)) {',
    '  failures.push("Missing route registry: src/lib/route-registry.ts");',
    '} else {',
    '  const routeRegistryContents = readFileSync(routeRegistryPath, "utf8");',
    '  for (const entry of expectedHeroVariants) {',
    '    if (!routeRegistryContents.includes(entry.heroVariant)) {',
    '      failures.push(`Hero variant ${entry.heroVariant} not referenced in src/lib/route-registry.ts`);',
    '    }',
    '  }',
    '  evidence.push("route-registry -> src/lib/route-registry.ts");',
    '}',
    '',
    'for (const componentName of expectedComponents) {',
    '  const fileName = componentName',
    "    .replace(/([a-z])([A-Z])/g, '$1-$2')",
    '    .toLowerCase();',
    '  const relativePath = `src/components/${fileName}.tsx`;',
    '  if (!existsSync(path.join(rootDir, relativePath))) {',
    '    failures.push(`Missing component file: ${relativePath}`);',
    '  } else {',
    '    evidence.push(`component:${componentName} -> ${relativePath}`);',
    '  }',
    '}',
    '',
    'const report = {',
    '  status: failures.length === 0 ? "passed" : "failed",',
    '  failures,',
    '  evidence',
    '};',
    '',
    'mkdirSync(path.join(rootDir, "reports"), { recursive: true });',
    'writeFileSync(path.join(rootDir, "reports", "frontend-self-audit.json"), JSON.stringify(report, null, 2));',
    '',
    'if (failures.length > 0) {',
    '  throw new Error(failures.join("\\n"));',
    '}',
    '',
    "console.log('Frontend self-audit passed and evidence was written to reports/frontend-self-audit.json.');",
    ''
  ].join('\n');
}

